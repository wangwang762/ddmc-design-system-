// 颜色和文字是业务属性锁定的，不可分离
// 每种类型对应唯一的背景色和显示文字
export type 标题前标签类型 =
  | '疯抢'     // 红色   #FC5455
  | '预售'     // 绿色   #00B740
  | '新品'     // 亮绿色 渐变 #9DFF4C → #CBF529（深色文字）
  | '趋势'     // 橙色   #FF8A00
  | '宝妈严选' // 粉色   #FE6999
  | '叮咚心选' // 金色   #C9A856
  | '美麒麟'   // 深红色 #EA333E

export interface 标题前标签Props {
  /** 业务类型，决定颜色和文字，两者锁定不可分离 */
  类型: 标题前标签类型
  /**
   * 小 = 商品卡片（10px / padding 1.5px 3px）
   * 大 = 商品详情页（12px / padding 2px 4px）
   * 默认 "小"
   */
  尺寸?: '小' | '大'
}
