export type 商品卡片布局 = '单列' | '单列定高' | '双列' | '双列定高' | '三列' | '泳道' | '新人专区商卡'

// 标题前标签直接复用独立组件的类型（颜色与文字业务锁定）
import type { 标题前标签类型 } from '../标题前标签/types'
export type 标题前标签 = 标题前标签类型

/**
 * 营销标签类型：
 * - 绿色底：配送标签，如"明日送达" — 绿底描边+绿色文字
 * - 粉红底：促销标签，如"特惠"/"限时抢" — 粉红描边+红色文字
 * - 橙色底：如"买过" — 橙底描边+橙色文字
 * - 蓝色底：如"最快明日送达" — 蓝底描边+蓝色文字
 * - 省：满减标签，如"省1.8元" — 实心红左块 + 描边右块
 * - 折：折扣标签，如"6.58折" — 实心红左块 + 描边右块
 * - 绿卡返现：如"返0.3元" — 渐变绿左块(叶子图标) + 金色描边右块
 * - 近N天低价：如"近14天低价" — 粉底+闪电图标+红色文字
 * - 疯抢专享价：渐变红底图片标签，无需传文字
 */
export type 营销标签类型 =
  | '绿色底'
  | '粉红底'
  | '橙色底'
  | '蓝色底'
  | '省'
  | '折'
  | '绿卡返现'
  | '近N天低价'
  | '疯抢专享价'

export interface 营销标签 {
  类型: 营销标签类型
  /** 疯抢专享价 不需要传文字（图片内置），其余类型必填 */
  文字?: string
}

/**
 * 商品卡片 — 5 种布局共用同一套 props，通过 `布局` 切换渲染：
 *
 * ```tsx
 * <商品卡片
 *   布局="双列"
 *   图片="https://..."
 *   标题="气泡水500ml"
 *   价格="38.76"
 *   单位="/份"
 *   星级
 *   利益点="利益点｜利益点｜利益点"
 *   营销标签={[
 *     { 类型: '绿色底', 文字: '明日送达' },
 *     { 类型: '省',     文字: '省1.8元'  },
 *   ]}
 *   下单数="2.1万人已下单"
 *   榜单横幅="新鲜水果榜第1名"
 *   onCartClick={() => addToCart(id)}
 * />
 * ```
 */
export interface 商品卡片Props {
  /** 卡片布局类型 */
  布局: 商品卡片布局
  /** 商品图片 URL */
  图片: string
  /** 商品标题 */
  标题: string
  /** 价格数值字符串，如 "38.76"，内部自动拆分整数/小数按 Figma 规格渲染 */
  价格: string
  /** 计量单位，默认 "/份" */
  单位?: string
  /** 标题前浮动彩色标签（如"疯抢"/"美麒麟"），最多 2 个 */
  标题前标签?: 标题前标签[]
  /** 是否显示"五星"品质徽标（绿色背景）*/
  星级?: boolean
  /** 利益点文案，如 "利益点｜利益点｜利益点" */
  利益点?: string
  /** 推荐理由（单列定高专用），如 "免费宰杀｜高蛋白｜干净无沙" */
  推荐理由?: string
  /** 营销标签列表（明日送达 / 特惠 / 省X元等） */
  营销标签?: 营销标签[]
  /** 下单人数文案，如 "2.1万人已下单" — 金色字 */
  下单数?: string
  /** 榜单横幅文案（单列/双列），如 "新鲜水果榜第1名" — TOP图标+文字+箭头 */
  榜单横幅?: string
  /** 榜单标签文案（单列定高专用），如 "水产海鲜榜 第1名" — 金色纯文字标签 */
  榜单标签?: string
  /** 划线原价，如 \"40.76\" — 泳道 / 新人专区商卡 专用，显示在现价下方 */
  原价?: string
  /** 购物车 badge 数字 */
  购物车badge?: number
  /** 点击卡片整体 */
  onClick?: () => void
  /** 点击购物车按钮 */
  onCartClick?: () => void
  className?: string
}
