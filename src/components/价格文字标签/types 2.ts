/** 粉底价格 = #FFEFEF 底色 + DdmcSans 红色价格，可带划线原价
 *  粉底文字 = #FFEFEF 底色 + 红色文字，用于"尝鲜价¥16.55"等
 *  红底文字 = #FF3133 底色 + 白色文字，用于"补贴价¥99.9"等强调场景
 */
export type 价格文字标签风格 = '粉底价格' | '粉底文字' | '红底文字'

export interface 价格文字标签Props {
  /** 展示风格 */
  风格: 价格文字标签风格
  /** 价格数字字符串，风格='粉底价格' 时使用，如 "6.5" */
  价格?: string
  /** 划线原价，风格='粉底价格' 时可选，如 "23.88" */
  原价?: string
  /** 文字内容，风格='粉底文字'|'红底文字' 时使用，如 "尝鲜价¥16.55" */
  文字?: string
  className?: string
}
